package 필기;

public class 캐스팅 {
	
	public static void main(String[] args) {
		
		// String -> int 변환
		// 문자열을 숫자타입으로 바꿔주쎄용
		String num = "10";
		System.out.print(Integer.parseInt(num)+1);
		
		
		
		// << Casting >>
		// - 기존 데이터 타입을 다른 데이터타입으로 변환하는 것
		
		
		// Upcasting
		// - 하위 클래스가 상위 클래스 타입으로 자동 타입 변환하는 것
		// - 하위 클래스 객체 내 모든 변수, 메소드에 접근할 수 없고 상위 클래스의 변수, 메소드에만 접근 가능
		// - 단, 하위 클래스가 상위 클래스의 메소드를 오버라이딩 한 경우 하위 클래스의 메소드를 호출
		
		
		
		
		
		// Downcasting
		
		
	}
	

}
